    <h2>How to play</h2>

    <ul>
        <li>
          <strong><h3>1. Start:</h3> </strong> 
          The game begins with an even number of cards laid face down in a grid. <br>
          The cards are arranged in identical pairs,and the player cannot see the back <br>
          of the cards until they are flipped over.
        </li>

        <li>
            <strong> <h3>2. Objective of the Game: </h3> </strong> 
            The main objective is to find all matching pairs<br>
            of cards in the fewest number of moves possible.
        </li>
    </ul>

    <h2>How It Works</h2>

    <ul>
        <li>
            <strong> <h3> 1. Flipping Cards: </h3></strong> 
            On each turn, the player flips two cards (one at a time). 
            The flipped<br>
            cards are shown to the player, revealing their images or symbols.
        </li>
        
        <li>
            <strong> <h3> 2. Checking for a Match: </h3></strong> 
            If the two flipped cards are identical (they form a pair), they remain<br>
            face up (revealed) and are considered a match. If the cards are not<br>
            identical, they are flipped back down, hiding their images again.
        </li>

        <li>
            <strong><h3> 3. End of the Game: </h3></strong> 
            The game ends when all pairs of cards have been matched.
            The winner is the<br>
            player who has found all the pairs, or, if played in competitive mode,<br>
            the one who has found the most pairs.
        </li>
    </ul>

